import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { minifyJs, minifyCss } from "./minify-web-assets.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const contentDir = path.join(root, "src", "web-ui", "content");
const outFile = path.join(root, "src", "web-ui", "embedded-assets.ts");

const assets = [
  ["scriptsJs", "scripts.js", "application/javascript"],
  ["stylesCss", "styles.css", "text/css; charset=utf-8"],
  ["wtermJs", path.join("vendor", "wterm", "wterm.bundle.js"), "application/javascript"],
  ["wtermCss", path.join("vendor", "wterm", "terminal.css"), "text/css; charset=utf-8"],
  ["qrcodeJs", path.join("vendor", "qrcode", "qrcode.bundle.js"), "application/javascript"],
];

const entries = {};
const hash = createHash("sha256");
for (const [key, relPath, contentType] of assets) {
  let content = readFileSync(path.join(contentDir, relPath));
  if (relPath === "scripts.js") {
    content = Buffer.from(minifyJs(content.toString("utf8")), "utf8");
  } else if (relPath === "styles.css") {
    content = Buffer.from(minifyCss(content.toString("utf8")), "utf8");
  }
  const assetHash = createHash("md5").update(content).digest("hex").slice(0, 8);
  hash.update(relPath).update("\0").update(content).update("\0");
  entries[key] = {
    base64: content.toString("base64"),
    hash: assetHash,
    contentType,
  };
}

const assetVersion = hash.digest("hex").slice(0, 12);

const source = `// Generated by scripts/generate-web-assets.js. Do not edit by hand.
import { Buffer } from "node:buffer";

function decode(value: string): string {
  return Buffer.from(value, "base64").toString("utf8");
}

export const EMBEDDED_WEB_ASSET_VERSION = ${JSON.stringify(assetVersion)};

export const EMBEDDED_WEB_ASSETS = {
  scriptsJs: decode(${JSON.stringify(entries.scriptsJs.base64)}),
  stylesCss: decode(${JSON.stringify(entries.stylesCss.base64)}),
  vendor: {
    "/vendor/wterm/wterm.bundle.js": {
      content: decode(${JSON.stringify(entries.wtermJs.base64)}),
      contentType: ${JSON.stringify(entries.wtermJs.contentType)},
      hash: ${JSON.stringify(entries.wtermJs.hash)},
    },
    "/vendor/wterm/terminal.css": {
      content: decode(${JSON.stringify(entries.wtermCss.base64)}),
      contentType: ${JSON.stringify(entries.wtermCss.contentType)},
      hash: ${JSON.stringify(entries.wtermCss.hash)},
    },
    "/vendor/qrcode/qrcode.bundle.js": {
      content: decode(${JSON.stringify(entries.qrcodeJs.base64)}),
      contentType: ${JSON.stringify(entries.qrcodeJs.contentType)},
      hash: ${JSON.stringify(entries.qrcodeJs.hash)},
    },
  },
} as const;

export type EmbeddedVendorAssetPath = keyof typeof EMBEDDED_WEB_ASSETS.vendor;
`;

writeFileSync(outFile, source, "utf8");
console.log(`[generate-web-assets] wrote ${path.relative(root, outFile)} (${assetVersion})`);
